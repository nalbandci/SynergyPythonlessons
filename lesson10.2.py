"""
Задание №2

Создать словарь, где ключи — целые числа от 10 до -5 (включительно), 
а значения — эти числа, возведённые в степень, равную самим числам.
"""

# Создаем список чисел от 10 до 1 и от -1 до -5, чтобы исключить 0 (0^0 вызывает ошибку)
numbers = list(range(10, 0, -1)) + list(range(-1, -6, -1))

# Генерируем словарь через dict comprehension
my_dict = {
    num: num ** num
    for num in numbers
}

# Выводим результат (для компактности показаны только первые и последние элементы)
print({
    10: my_dict[10],
    9: my_dict[9],
    "...": "...",
    -5: my_dict[-5]
})