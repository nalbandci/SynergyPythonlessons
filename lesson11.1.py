# Описание задачи
# 1. Вычислить факториал входного числа.
# 2. Создать список факториалов чисел от полученного значения до 1 в убывающем порядке.

def factorial(n):
    """Вычисляет факториал числа n."""
    if n == 0:
        return 1  # Факториал 0 равен 1
    result = 1
    for i in range(1, n + 1):
        result *= i  # Умножаем result на каждое число от 1 до n
    return result

def get_factorial_list(num):
    """Создает список факториалов от факториала num до 1."""
    start = factorial(num)  # Шаг 1: Вычисляем факториал входного числа
    # Шаг 2: Для каждого числа от start до 1 (включительно) вычисляем его факториал
    return [factorial(n) for n in range(start, 0, -1)]

# Пример использования
print(get_factorial_list(3))  # Вывод: [720, 120, 24, 6, 2, 1]