"""
Класс Касса для управления денежными операциями.


Пояснение к классу CashRegister:
1. __init__: задает начальный баланс (по умолчанию 0).
2. top_up: добавляет переданную сумму к балансу.
3. take_away: проверяет, достаточно ли средств перед изъятием.
4. count_1000: показывает, сколько полных тысяч есть в кассе.
"""
class CashRegister:
    def __init__(self, initial=0):
        self.money = initial  # Инициализация начальной суммы денег

    def top_up(self, X):
        """Пополнить кассу на сумму X."""
        self.money += X  # Увеличиваем текущий баланс

    def take_away(self, X):
        """Изъять сумму X из кассы. Вызывает ошибку при недостатке средств."""
        if self.money < X:
            raise ValueError("Недостаточно денег в кассе")  # Проверка наличия средств
        self.money -= X  # Уменьшаем текущий баланс

    def count_1000(self):
        """Возвращает количество целых тысяч в кассе."""
        return self.money // 1000  # Целочисленное деление на 1000

# Пример использования класса CashRegister
kassa = CashRegister(5500)
kassa.top_up(500)  # Баланс: 6000
print(kassa.count_1000())  # 6 тысяч
kassa.take_away(3000)  # Баланс: 3000