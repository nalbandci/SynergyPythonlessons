# Задача: Написать программу, которая принимает натуральное число X (где X ≤ 2e9) 
# и подсчитывает количество его натуральных делителей, включая 1 и само число.

def count_divisors(x):
    count = 0  
    # Перебираем числа от 1 до квадратного корня X
    for i in range(1, int(x**0.5) + 1):
        # Проверяем, является ли i делителем X
        if x % i == 0:
            count += 1  # Увеличиваем счетчик для делителя i
            if i != x // i:
                count += 1 # Увеличиваем счетчик для соответствующего делителя x // i
    return count  # Возвращаем общее количество делителей

# Ввод натурального числа X
X = int(input("Введите натуральное число X (x ≤ 2e9): "))

# Проверяем, что число X в допустимом диапазоне
if X > 0 and X <= 2 * 10**9:
    num_divisors = count_divisors(X)  # Вызываем функцию для подсчета делителей
    print(f"Количество натуральных делителей числа {X}: {num_divisors}")
else:
    print("Число должно быть натуральным и не превышать 2 миллиарда.")