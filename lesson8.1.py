# Программа для переворота массива чисел.

# Вводим число N
N = int(input("Введите количество чисел N (1 ≤ N ≤ 10000): "))

# Проверка диапазона N
if N < 1 or N > 10000:
    print("Ошибка: N должно быть в диапазоне от 1 до 10000.")
else:
    # Создаем список для хранения чисел
    numbers = []

    # Вводим N чисел
    for i in range(N):
        number = int(input(f"Введите число {i + 1} (по модулю не больше 10^5): "))
        
        # Проверка диапазона каждого числа
        if abs(number) > 10**5:
            print("Ошибка: Число должно быть по модулю не больше 10^5.")
            break  # Завершение программы, если число не соответствует требованиям
        else:
            numbers.append(number)  # Добавляем число в список

    else:  # Этот else связан с циклом for. Он выполнится, если не было вызвана команда break.
        # Переворачиваем список чисел
        reversed_numbers = numbers[::-1]

        # Выводим перевернутый массив
        print("Перевернутый массив:", ' '.join(map(str, reversed_numbers)))