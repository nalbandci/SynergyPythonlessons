# Задача: Написать функцию, которая получает на вход массив чисел и изменяет его таким образом,
# чтобы на первом месте стоял последний элемент, на втором - первый, на третьем - второй и т. д.
# Входные данные: число N (1 ≤ N ≤ 100 000) и N чисел (1 ≤ Ai ≤ 10e9).
# Выходные данные: изменённый массив.


# Шаг 1: Считываем количество элементов
n = int(input("Введите количество элементов массива (N): "))

# Шаг 2: Считываем строку с числами и разбиваем её на список подстрок
# Пример: для ввода "1 2 3 4" получим arr = ['1', '2', '3', '4']
arr = input("Введите элементы массива через пробел: ").split()

# Шаг 3: Формируем новый массив
# - Берём последний элемент исходного массива: arr[-1]
# - Добавляем все элементы исходного массива, кроме последнего: arr[:-1]
# Пример: arr[-1] = '4', arr[:-1] = ['1', '2', '3'] → result = ['4', '1', '2', '3']
result = [arr[-1]] + arr[:-1]

# Шаг 4: Объединяем элементы списка в строку через пробел и выводим
# Пример: ' '.join(['4', '1', '2', '3']) → "4 1 2 3"
print(' '.join(result))