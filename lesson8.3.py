# Задача:
# На берегу реки стояли n рыбаков, все они хотели перебраться на другой берег.
# Одна лодка может выдержать не более m килограмм, при этом в лодку помещается не более 2 человек.
# Определите, какое минимальное число лодок нужно, чтобы перевезти на другой берег всех рыбаков.
# В первую строку вводится число m (1 ≤ m ≤ 10e6) - максимальная масса, которую может выдержать одна лодка.
# Во вторую строку вводится число n (1 ≤ n ≤ 100) - количество рыбаков.
# В следующие N строк вводится по одному числу Ai (1 ≤ Ai ≤ m) - вес каждого путешественника.
# Программа должна вывести одно число - минимальное количество лодок, необходимое для переправки всех рыбаков на противоположный берег.

def min_boats_needed(max_weight, num_fishers, weights):
    # Сортируем веса рыбаков в порядке возрастания
    weights.sort()
    
    # Инициализируем указатели для начала и конца списка
    left = 0      # Указатель на самого легкого рыбака
    right = num_fishers - 1  # Указатель на самого тяжёлого рыбака
    boats_count = 0  # Счётчик для числа лодок

    # Пока есть рыбак на левой стороне
    while left <= right:
        # Проверяем, можем ли мы отправить двух рыбаков в одной лодке
        if weights[left] + weights[right] <= max_weight:
            # Если да, перемещаем указатели
            left += 1  # Самый легкий рыбак сажается в лодку
        # Перемещаем указатель на самого тяжёлого рыбака
        right -= 1  # Самый тяжёлый рыбак сажается в лодку
        # Увеличиваем счётчик лодок после каждой отправки
        boats_count += 1

    return boats_count

# Ввод данных
if __name__ == "__main__":
    m = int(input("Введите максимальный вес лодки: "))  # Ввод максимального веса, который может выдержать лодка
    n = int(input("Введите количество рыбаков: "))  # Ввод количества рыбаков
    weights = []  # Список для хранения весов рыбаков

    # Ввод весов рыбаков
    for _ in range(n):
        weight = int(input("Введите вес рыбака: "))  # Ввод веса каждого рыбака
        weights.append(weight)  # Добавляем вес в список

    # Вычисляем и выводим минимальное количество лодок
    result = min_boats_needed(m, n, weights)
    print(result)  # Выводим результат