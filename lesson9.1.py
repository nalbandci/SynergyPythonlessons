"""
Задача:
Определить количество уникальных чисел в списке.

Ввод:
1. N - количество чисел (1 ≤ N ≤ 100000).
2. N целых чисел через пробел, каждое по модулю не превышает 2*10^9.

Вывод:
Количество уникальных чисел.
"""

try:
    # Шаг 1: Ввод количества чисел с проверкой диапазона
    n = int(input("Введите количество чисел (1 ≤ N ≤ 100000): "))
    if n < 1 or n > 100000:
        raise ValueError("N должно быть в диапазоне от 1 до 100000.")
except ValueError as e:
    print(f"Ошибка: {e}")
    exit()

# Шаг 2: Ввод чисел с проверкой их количества
numbers_str = input("Введите числа через пробел: ").split()
if len(numbers_str) != n:
    print(f"Ошибка: Введено {len(numbers_str)} чисел вместо {n}.")
    exit()

try:
    # Преобразуем строки в целые числа
    numbers = list(map(int, numbers_str))
except ValueError:
    print("Ошибка: Введены некорректные данные (не целые числа).")
    exit()

# Проверка на превышение модуля числа
for num in numbers:
    if abs(num) > 2 * 10**9:
        print(f"Ошибка: Число {num} превышает 2*10^9 по модулю.")
        exit()

# Шаг 3: Подсчет уникальных чисел через множество
unique_numbers = set(numbers)
print("Количество уникальных чисел:", len(unique_numbers))