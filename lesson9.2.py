"""
Задача:
Определить количество уникальных чисел в списке.

Ввод:
1. N - количество чисел (1 ≤ N ≤ 100 000).
2. N целых чисел через пробел (каждое число по модулю ≤ 2*10^9).

Вывод:
Количество уникальных чисел.
"""

# ------------------------- Шаг 1: Ввод количества чисел (N) -------------------------
try:
    # Запрашиваем N у пользователя с подсказкой
    n = int(input("Введите количество чисел (от 1 до 100 000): "))
    
    # Проверяем, что N в допустимом диапазоне
    if n < 1 or n > 100000:
        raise ValueError("Ошибка: N должно быть от 1 до 100 000.")
except ValueError as e:
    print(e)  # Выводим сообщение об ошибке
    exit()     # Завершаем программу

# ------------------------- Шаг 2: Ввод чисел через пробел -------------------------
numbers_input = input("Введите числа через пробел (например: 5 -3 10 5): ").split()

# Проверяем, что введено ровно N чисел
if len(numbers_input) != n:
    print(f"Ошибка: Вы ввели {len(numbers_input)} чисел, а должно быть {n}.")
    exit()

# ------------------------- Шаг 3: Преобразование строк в целые числа -------------------------
try:
    numbers = list(map(int, numbers_input))  # Пробуем преобразовать в числа
except ValueError:
    print("Ошибка: Введены некорректные данные (должны быть целые числа).")
    exit()

# ------------------------- Шаг 4: Проверка на ограничение (|число| ≤ 2*10^9) -------------------------
for num in numbers:
    if abs(num) > 2 * 10**9:
        print(f"Ошибка: Число {num} превышает 2*10^9 по модулю.")
        exit()

# ------------------------- Шаг 5: Подсчет уникальных чисел -------------------------
unique_numbers = set(numbers)  # Множество автоматически удаляет дубликаты
print("Количество уникальных чисел:", len(unique_numbers))