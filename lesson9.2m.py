"""
Задача:
Определить, сколько уникальных чисел присутствует одновременно в двух списках.

Ввод:
- Первый список: числа вводятся построчно, каждое на отдельной строке. 
  Ввод завершается пустой строкой.
- Второй список: аналогично первому.

Вывод:
Целое число - количество уникальных чисел, встречающихся в обоих списках.
"""

# Шаг 1: Чтение первого списка чисел
print("Введите числа первого списка (каждое на новой строке). Для завершения ввода нажмите Enter:")
list1 = []
while True:
    line = input().strip()  # Считываем строку и удаляем пробелы по краям
    if line == "":  # Если введена пустая строка, завершаем ввод
        break
    try:
        num = int(line)  # Пробуем преобразовать строку в целое число
        list1.append(num)  # Добавляем число в список
    except ValueError:
        print(f"Ошибка: '{line}' не является целым числом. Пожалуйста, вводите только целые числа.")
        exit()  # Завершаем программу при ошибке ввода

# Шаг 2: Чтение второго списка чисел (аналогично первому)
print("\nВведите числа второго списка (каждое на новой строке). Для завершения ввода нажмите Enter:")
list2 = []
while True:
    line = input().strip()
    if line == "":
        break
    try:
        num = int(line)
        list2.append(num)
    except ValueError:
        print(f"Ошибка: '{line}' не является целым числом. Пожалуйста, вводите только целые числа.")
        exit()

# Шаг 3: Преобразование списков в множества для эффективного поиска пересечений
set1 = set(list1)  # Множество уникальных чисел из первого списка
set2 = set(list2)  # Множество уникальных чисел из второго списка

# Шаг 4: Нахождение общих чисел (пересечение множеств)
common_numbers = set1 & set2  # Альтернатива: set1.intersection(set2)

# Шаг 5: Вывод количества общих чисел
print("\nРезультат:")
print(len(common_numbers))