"""
Задача:
Для каждого числа в последовательности определить, встречалось ли оно ранее.
Вывести "YES" или "NO" для каждого числа в отдельной строке.
"""

# Запрашиваем ввод данных у пользователя с подсказкой
numbers_input = input("Введите числа через пробел (например: 1 2 3 2 3 4): ").split()

# Создаем множество для отслеживания уникальных чисел
seen_numbers = set()

# Создаем список для хранения результатов
results = []

# Проходим по каждому числу в введенной последовательности
for num_str in numbers_input:
    try:
        # Пробуем преобразовать строку в целое число
        num = int(num_str)
    except ValueError:
        # Если не получилось, пробуем преобразовать в вещественное
        try:
            num = float(num_str)
        except ValueError:
            # Если оба преобразования не удались, сообщаем об ошибке
            print(f"Ошибка: '{num_str}' не является числом.")
            exit()

    # Проверяем, было ли число уже в множестве
    if num in seen_numbers:
        results.append("YES")
    else:
        results.append("NO")
        seen_numbers.add(num)  # Добавляем новое число в множество

# Выводим результаты построчно
print("\n".join(results))